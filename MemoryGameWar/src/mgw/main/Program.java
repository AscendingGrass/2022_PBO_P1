    private void cardClicked(java.awt.event.MouseEvent evt){
        if(evt.getSource() instanceof Card c){
            if(c.clicked)
                return;
            if(Deck.counter > 5)
                return;
            for(Deck i : deck)
                if(i.skill == null){
                    i.deckCopy(c);
                    return;
                }
        }
    }
    
